public class operators {
    public static void main(String[] args) {
        //arithmetic operator:
        int a = 5;
        int b = 6;
//        System.out.println(b);
        //assignment operators:
        // =,+=,*= etc.
        a += 20;
         b += 9;
        System.out.println(b);
        System.out.println(a);
        //comparison operator:
        System.out.println(64<6);  //statement is false
        //logical operators:
        System.out.println(65>5 && 89>26);  //both statement must true to show true as output
        System.out.println(65>5 || 89<26);  //both statement must not be true to show true as output
    }
}
